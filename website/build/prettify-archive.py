import bs4
import copy
import io
import sys
import tarfile

input_tarball  = tarfile.open(sys.argv[1])
output_tarball = tarfile.open(sys.argv[2], 'w')

def normalize_string(string):
    # Unbreak strings as otherwise this produces a lot of
    # differences.
    string = string.replace("\n", " ")

    # Normalize some characters in lbssg to miniimze the
    # differeces.
    string = string.replace('“', '"')
    string = string.replace('”', '"')
    string = string.replace("’", "'")
    string = string.replace("‘", "'")
    string = string.replace("–", "--")
    string = string.replace("…", "...")
    return string

for path in input_tarball:
    if path.isfile() and path.name.endswith(".html"):
        original_html = input_tarball.extractfile(path).read().decode('utf-8')

        soup = bs4.BeautifulSoup(normalize_string(original_html),
                                 'html5lib', from_encoding='utf-8')

        # We need 2 pass of prettification: one before and one after.
        pretty_html = bytes(soup.prettify(), 'utf-8')
        soup = bs4.BeautifulSoup(pretty_html, 'html.parser')

        # TODO: do we still need that if we normalize the content
        # before parsing?
        # Normalize content
        for elm in soup.find_all():
            if 'string' in dir(elm) and elm.string != None:
                elm.string = normalize_string(elm.string)

        # Do we still need that if we normalize the content before parsing?
        final_html = bytes(soup.prettify(), 'utf-8')

        path.size = len(final_html)
        memfd = io.BytesIO(final_html)
        output_tarball.addfile(path, memfd)

    elif path.isfile() and path.name.endswith(".md"):
        # Don't keep markdown as the files will differ between Untitled and
        # Haunt due to different metadata delimiters. There should be no other
        # differences beside that so it's not very important to keep them to
        # compare the resulting websitess.
        pass
    elif path.isfile() and path.name.endswith(".date"):
        # The .date files are generated by Untitled to not rebuild files
        # unnecessarily. We don't need these to compare the resulting websites.
        pass
    else:
        output_tarball.addfile(path, input_tarball.extractfile(path))

input_tarball.close()
output_tarball.close()
